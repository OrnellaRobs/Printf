#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	unsigned int	i;
	size_t			nb;
	char			*str;

	i = 0;
	if (s == NULL)
		return (NULL);
	nb = ft_strlen(s);
	if (!(str = (char *)malloc(sizeof(char) * (nb + 1))))
		return (NULL);
	while (s[i])
	{
		str[i] = f(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
